/** Return bilingual label: "English / NativeLang" or just "English" for 'en'. */
export function tl(key: string, lang: LangCode): string {
  const map = T[key]
  if (!map) return key
  if (lang === 'en') return map.en
  const native = map[lang]
  if (!native || native === map.en) return map.en
  return `${map.en} / ${native}`
}

// ─────────────────────────────────────────────────────────────────────────────
// Zustand store — language + view mode
// ─────────────────────────────────────────────────────────────────────────────

interface LangState {
  lang: LangCode
  setLang: (l: LangCode) => void
  viewMode: ViewMode
  setViewMode: (m: ViewMode) => void
  toggleViewMode: () => void
}

export const useLangStore = create<LangState>()(
  persist(
    (set) => ({
      lang: 'en',
      setLang: (lang) => set({ lang }),
      viewMode: 'desktop',
      setViewMode: (viewMode) => set({ viewMode }),
      // Use functional set so we always read the current state, never stale closure
      toggleViewMode: () =>
        set((state) => ({
          viewMode: (state.viewMode ?? 'desktop') === 'desktop' ? 'mobile' : 'desktop',
        })),
    }),
    {
      name: 'zel-eye-lang',
      storage: createJSONStorage(() => localStorage),
      // Custom merge: if stored state is missing a field (e.g. viewMode from old schema),
      // fall back to the initial state value instead of setting it to undefined.
      merge: (persisted, current) => {
        const p = (persisted ?? {}) as Partial<LangState>
        return {
          ...current,
          lang:     p.lang     ?? current.lang,
          viewMode: p.viewMode ?? current.viewMode,
        }
      },
    },
  ),
)

export const useLang = () => useLangStore((s) => s.lang)
export const useViewMode = () => useLangStore((s) => s.viewMode)
